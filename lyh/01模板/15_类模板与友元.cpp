#include <string>
#include <iostream>
using namespace std;

// ==========================================
// 1. 前置声明 (Forward Declarations)
// ==========================================

// 【为什么要写这句？】
// 因为下面的全局函数 printPerson2 用到了 Person 类。
// 我们需要提前告诉编译器：“后面有一个叫 Person 的类模板，你先有个印象，别报错。”
template <class T1, class T2>
class Person;

// ==========================================
// 2. 全局函数模板定义 (类外实现)
// ==========================================

// 【这是一个全局函数模板】
// 作用：在类外部打印 Person 对象的属性。
// 参数：Person<T1, T2> & p
//       - Person<T1, T2>：表示传入的 Person 对象必须与本函数的模板参数类型 (T1, T2) 保持一致。
//       - & p：使用引用传递，避免拷贝整个对象，提高运行效率。
template <class T1, class T2>
void printPerson2(Person<T1, T2> &p)
{
    // 能够直接访问 p.m_Name 和 p.m_Age，是因为在 Person 类内部将其声明为了 friend (友元)
    cout << "类外实现 ---- 姓名： " << p.m_Name << " 年龄：" << p.m_Age << endl;
}

// ==========================================
// 3. 类模板定义
// ==========================================

template <class T1, class T2>
class Person
{
    // ------------------------------------------
    // 【友元声明 1：全局函数类内实现】
    // ------------------------------------------
    // 作用：声明一个全局友元函数 printPerson，并直接在类内完成它的实现。
    // 参数：Person<T1, T2> & p -> 传入当前 Person 实例化对象的引用。
    // 特点：虽然写在类内部，但它不是成员函数，调用时不需要通过对象（不用 p.printPerson()）。
    friend void printPerson(Person<T1, T2> &p)
    {
        // 友元函数拥有“特权”，可以直接读取私有成员 m_Name 和 m_Age
        cout << "姓名： " << p.m_Name << " 年龄：" << p.m_Age << endl;
    }

    // ------------------------------------------
    // 【友元声明 2：全局函数类外实现】
    // ------------------------------------------
    // 作用：告诉类模板，外面有一个叫 printPerson2 的函数模板是我的“好朋友”，允许它访问我的私有成员。
    // 参数：Person<T1, T2> & p -> 传入 Person 对象引用。
    // 注意：printPerson2 后面必须加 <>，表示这是一个“函数模板”的特化声明，否则编译器会把它当成普通函数。
    //在24行已经声明了函数模板，如果没有<>，那么编译器会去找类外找普通模板，显然是找不到的
    friend void printPerson2<>(Person<T1, T2> &p);

public:
    // ------------------------------------------
    // 【构造函数】
    // ------------------------------------------
    // 作用：初始化 Person 对象的成员变量。
    // 参数：
    //   - T1 name：姓名，类型由模板参数 T1 决定（例如可以是 string）。
    //   - T2 age：年龄，类型由模板参数 T2 决定（例如可以是 int）。
    Person(T1 name, T2 age)
    {
        this->m_Name = name; // 将传入的 name 赋值给私有属性 m_Name
        this->m_Age = age;   // 将传入的 age 赋值给私有属性 m_Age
    }

private:
    // ------------------------------------------
    // 【私有成员变量】
    // ------------------------------------------
    T1 m_Name; // 姓名，类型由实例化时的 T1 决定
    T2 m_Age;  // 年龄，类型由实例化时的 T2 决定
};

// ==========================================
// 4. 测试函数与主函数
// ==========================================

// 【测试函数 1】：测试“类内实现”的友元函数
void test01()
{
    // 实例化一个 Person 对象 p，T1 替换为 string，T2 替换为 int
    Person<string, int> p("Tom", 20);

    // 调用全局函数 printPerson，并将对象 p 作为参数传入
    printPerson(p);
}

// 【测试函数 2】：测试“类外实现”的友元函数
void test02()
{
    // 实例化一个 Person 对象 p，T1 替换为 string，T2 替换为 int
    Person<string, int> p("Jerry", 30);

    // 调用全局函数 printPerson2，并将对象 p 作为参数传入
    printPerson2(p);
}

int main()
{
    // 执行测试 2
    test02();

    system("pause"); // 暂停控制台（防止窗口一闪而过）
    return 0;
}